    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return (targetSheet?.groups.count)!
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return (targetSheet?.groups[section].events.count)!
    }
    
    override func tableView(_ tableView: UITableView, titleForHeaderInSection section: Int) -> String? {
        return targetSheet?.groups[section].groupName
    }
    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "eventOption", for: indexPath)
        let event: Event = (targetSheet?.groups[indexPath.section].events[indexPath.row])!
        cell.textLabel?.text = event.eventName
        cell.detailTextLabel?.text = DateFormatter.localizedString(from: event.eventTime! as Date, dateStyle: .medium, timeStyle: .short)
        return cell
    }
    

